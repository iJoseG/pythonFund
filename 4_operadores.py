print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

print("\n\nDivisiom \"/\"")
print("6/3:")
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)
print("El resultado producido por el operador de division siempre es flotante, esto puede dar problemas en algunas situaciones, en que se requieran resultados enteros, por lo que Python puede ayudar con esto: ")

print("\n\nDivision entera \"//\"")
print("6 // 3:")
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)
print("6 // 4:")
print(6 // 4)
print(6. // 4)
print("6 / 4: Para comparar:")
print(6. / 4)
print("6 // 4: Con negativos: ")
print(-6 // -4)
print(6. // -4)

print("\n\nModulo-Residuo division entera \"%\"")
print("14 // 4:")
print(14 // 4)
print("14 % 4:")
print(14 % 4)

print("\n\nSuma \"-+\"")
print(-4 + 4)
print(-4. + 8)

print("\n\nResta Signo menos \"-\"")
print(-4 - 4)
print(4. - 8)
print(-1.1)

print("\n\nEnlaces y operadores ")
